// The goal of this program is to determine if knights on specific coordinates are able to 
// exist without being able to attack each other

import java.io.*;
import java.util.*;

public class SneakyKnights 
{

	// True or false for whether the knights are safe or not in their coordinates
	public static boolean allTheKnightsAreSafe(ArrayList<String> coordinateStrings, int boardSize)
	{
		if (coordinateStrings == null || boardSize == 0)
			return false;

		return true;
	}

	// Returns the difficulty rating
	public static double difficultyRating()
	{
		return 5.0;
	}

	// Returns the hours spent
	public static double hoursSpent()
	{
		return 4.0;
	}

}